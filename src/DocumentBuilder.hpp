#pragma once

#include <string>
#include <string_view>   // Для std::string_view (невладеющий просмотр строки)
#include <vector>

#include "Document.hpp"  // Нам нужен класс Document, возвращаемый из build

/**
 * @brief Вспомогательный класс для подготовки документов.
 *
 * Предоставляет фабричный метод build, а также утилиты для обработки текста,
 * которые могут использоваться внутри InvertedIndex.
 */
class DocumentBuilder {
public:
    // ============================================================================
    //
    // std::string - ВЛАДЕЛЕЦ (владеет данными)
    // ========================================
    // - Выделяет память в куче для хранения строки
    // - Освобождает память в деструкторе (автоматически)
    // - Можно изменять содержимое (если не const)
    // - Размер объекта: ~24-32 байта (зависит от реализации)
    // - Копирование: МЕДЛЕННО (копирует все символы + выделяет новую память)
    // - Перемещение: БЫСТРО (передаёт указатель, не копируя данные)
    // - Риск висячего указателя: НЕТ (сам управляет своей памятью)
    // std::string_view - НАБЛЮДАТЕЛЬ (не владеет данными)
    // ==================================================
    // - НЕ выделяет память (просто хранит указатель и размер)
    // - НЕ освобождает память (не отвечает за время жизни данных)
    // - НЕ может изменять данные (только чтение)
    // - Размер объекта: ~16 байт (указатель + размер)
    // - Копирование: БЫСТРО (копирует только указатель и размер)
    // - Риск висячего указателя: ДА (если исходные данные удалены)
    //Наблюдатель не продлевает жизнь данных. Если владелец умрёт — наблюдатель укажет в пустоту!
    /**
     * @brief Создаёт Document из имени и текста.
     * @param name Название (string_view, не копируется, только читается).
     * @param text Текст документа (string_view, только просмотр).
     * @return Готовый Document с перемещением строк.
     *
     * Аргументы принимаются как std::string_view, что позволяет передавать
     * любые строковые данные: литералы, обычные std::string, временные объекты –
     * без ненужного копирования. Внутри создаются копии в std::string (владение).
     */
    // static перед методом означает, что метод принадлежит САМОМУ КЛАССУ,
    // а не отдельным объектам этого класса.
    //
    // Сравнение:
    // ==========
    // Обычный метод (без static)      | Статический метод (со static)
    // --------------------------------+----------------------------------
    // Вызывается на объекте:          | Вызывается на классе:
    // builder.build(...)              | DocumentBuilder::build(...)
    // Нужно создавать объект          | Не нужно создавать объект
    // Имеет доступ к this             | Не имеет this
    //
    // Почему метод build() статический?
    // ==================================
    // 1. Класс DocumentBuilder НЕ ХРАНИТ ДАННЫХ (у него нет полей)
    // 2. Создавать объект этого класса БЕССМЫСЛЕННО
    // 3. Метод просто создаёт и возвращает новый документ — это "ФАБРИКА"
    static Document build(std::string_view name, std::string_view text) {
        // Конструктор Document принимает по значению, поэтому строки будут созданы
        // здесь через конструктор std::string из string_view, а затем перемещены в Document.
        return Document(
            0,                      // Временный ID; настоящий ID присвоит индекс при добавлении
            std::string(name),      // Создаём владеющую строку из просмотра
            std::string(text)       // Аналогично для текста
        );
    }

    /**
     * @brief Разбивает текст на отдельные слова (токены).
     * @param text Невладеющий просмотр текста.
     * @return Вектор слов как std::string_view, указывающих на исходный текст.
     *
     * Возвращаемые string_view валидны, пока оригинальный text не уничтожен.
     * Мы не создаём новых строк, только "виды" на куски текста.
     */
    static std::vector<std::string_view> splitWords(std::string_view text);
    // ============================================================================
    // std::vector: ЧТО МОЖНО И ЧТО НЕЛЬЗЯ ДЕЛАТЬ
    // ============================================================================
    //
    // ┌─────────────────────────────────────────────────────────────────────────┐
    // │ ОПЕРАЦИЯ                      │ МОЖНО?│ ПРИМЕЧАНИЕ                      │
    // ├───────────────────────────────┼───────┼─────────────────────────────────┤
    // │ v[i] для существующего i      │ ✅    │ Без проверки границ (быстро)    │
    // │ v.at(i) для любого i          │ ✅    │ Бросает std::out_of_range       │
    // │ v[i] для i >= size()          │ ❌    │ Неопределённое поведение!       │
    // │ v.push_back(x)                │ ✅    │ Добавляет в конец               │
    // │ v.pop_back()                  │ ✅    │ Размер должен быть > 0          │
    // │ v.insert(it, x)               │ ✅    │ Инвалидирует итераторы          │
    // │ v.erase(it)                   │ ✅    │ Возвращает новый итератор       │
    // │ v.reserve(n)                  │ ✅    │ Только выделяет память          │
    // │ v.resize(n)                   │ ✅    │ Изменяет размер                 │
    // │ Изменять вектор в цикле       │ ⚠️    │ Только через it = v.erase(it)   │
    // │ Хранить сырые указатели       │ ⚠️    │ Инвалидируются при изменении    │
    // └───────────────────────────────┴───────┴─────────────────────────────────┘
    /**
     * @brief Приводит строку к нижнему регистру "на месте".
     * @param word Строка для модификации (владеющая).
     *
     * Используется, когда нужно нормализовать слово перед вставкой в индекс.
     */
    static void toLower(std::string& word);
};