#pragma once // Защита от повторного включения заголовка

#include <string> // Для std::string

/**
 * @brief Класс, представляющий один текстовый документ.
 *
 * Содержит идентификатор, имя и текст. Является "пассивным" хранилищем,
 * не выполняет никакой логики поиска/индексирования.
 */
class Document
{
  public:
    Document() = default;
    /**
     * @brief Конструктор.
     * @param id Уникальный номер документа.
     * @param name Название (например, имя файла).
     * @param text Полный текст документа.
     *
     * Параметры передаются по значению, чтобы разрешить перемещение (move)
     * при передаче временных объектов (rvalue). Внутри мы их "перемещаем"
     * в поля класса с помощью std::move, избегая копирования.
     */
    Document(int id, std::string name, std::string text)
        : id_(id) // Инициализация примитивного int (копирование, дешёвое)
          ,
          name_(std::move(name)) // Перемещаем строку name в поле name_
          ,
          text_(std::move(text)) // Перемещаем строку text в поле text_
    {
        // После перемещения параметры name и text всё ещё валидны, но их содержимое
        // (выделенная память) теперь принадлежит полям класса. Это позволяет избежать
        // глубокого копирования больших текстов.

        // std::move позволяет вместо копирования просто передать владение указателем на данные.
        //  Это критически важно для больших текстов документов, чтобы не тратить время и память на копирование.
        // Без std::move при передаче документа в индекс создавалась
        // бы полная копия всего текста. С std::move — передаётся только владение, текст остаётся на месте.
    }

    // Методы доступа (геттеры) – обеспечивают read-only доступ к полям.

    /**
     * @brief Возвращает идентификатор документа.
     * @return int id.
     */
    int id() const
    {
        return id_;
    }

    /**
     * @brief Возвращает имя документа по константной ссылке.
     * @return const std::string& (не копируем строку при возврате).
     */
    const std::string& name() const
    {
        return name_;
    }

    /**
     * @brief Возвращает текст документа по константной ссылке.
     * @return const std::string& (без копирования).
     */
    const std::string& text() const
    {
        return text_;
    }

  private:
    int id_ = 0;       // Уникальный идентификатор
    std::string name_; // Имя документа (владеет данными)
    std::string text_; // Содержимое (владеет данными)
};